package com.prinjsystems.grengine.utils;

public class FloatArray3 {
	
	private float a;
	private float b;
	private float c;
	
	public FloatArray3(float a, float b, float c) {
		this.a = a;
		this.b = b;
		this.c = c;
	}
	
	public float getA() {
		return a;
	}
	
	public float getB() {
		return b;
	}
	
	public float getC() {
		return c;
	}
	
	public void setArray(float a, float b, float c) {
		this.a = a;
		this.b = b;
		this.c = c;
	}
}
