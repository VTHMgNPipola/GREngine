package com.prinjsystems.grengine.utils;

public class DoubleArray3 {
	
	private double a;
	private double b;
	private double c;
	
	public DoubleArray3(double a, double b, double c) {
		this.a = a;
		this.b = b;
		this.c = c;
	}
	
	public double getA() {
		return a;
	}
	
	public double getB() {
		return b;
	}
	
	public double getC() {
		return c;
	}
	
	public void setArray(double a, double b, double c) {
		this.a = a;
		this.b = b;
		this.c = c;
	}
}
